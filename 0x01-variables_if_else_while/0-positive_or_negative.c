#include <stdlib.h>
#include <time.h>
#include <stdlib.h>
/**
 * main - Entry point
 * void - empty
 * Return: Always 0 (Success)
 */
int main(void)
{
	int n;

	if (n > 0)
	{
		printf("%d is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%d is zero\n", n);
	}
	else
	{
		printf("%d is negative\n", n);
	}
	return (0);
}
